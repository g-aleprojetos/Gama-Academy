//operadores aritiméticos

//adição +
var sumTwoNumbers = 1 + 3
console.log("1 + 3 = " + sumTwoNumbers)

//subtração -
var subTwoNumbers = 4 - 3
console.log("4 - 3 = " + subTwoNumbers)

//multiplicação *
var multTwoNumbers = 4 * 3
console.log("4 * 3 = " + multTwoNumbers)

//divisão /
var divTwoNumbers = 4 / 2
console.log("4 / 2 = " + divTwoNumbers)

//módulo ou resto de uma divisão %
var restTwoNumbers = 8 % 3
console.log("8 % 3 = " + restTwoNumbers)

//incremento ++
var num = 1;
console.log("num: " + num)
num++;
console.log("num: " + num)
console.log("num: " + ++num)

//decremento --
num--;
console.log("num: " + num)
console.log("num: " + --num)

